#' Extract a single character from a string, using its index.
#'
#' See [strex::str_elem()].
#'
#' @inheritParams strex::str_elem
#'
#' @export
str_elem <- strex::str_elem

#' @rdname str_elem
#' @export
elem <- str_elem

#' Extract several single elements from a string.
#'
#' See [strex::str_elems()].
#'
#' @inheritParams strex::str_elems
#'
#' @export
str_elems <- strex::str_elems

#' @rdname str_elems
#' @export
elems <- str_elems

#' Extract bits of a string and paste them together.
#'
#' See [strex::str_paste_elems()].
#'
#' @inheritParams strex::str_paste_elems
#'
#' @export
str_paste_elems <- strex::str_paste_elems

#' @rdname str_paste_elems
#' @export
paste_elems <- str_paste_elems
